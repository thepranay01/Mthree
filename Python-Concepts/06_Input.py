# input() statement is used to accept values(using keyboard) from user

#String input
name = input("Enter your name: ")

#int input
age = int(input("Enter your age: "))

#float input
salary = float(input("Enter your salary: "))

#boolean input
is_employee = bool(input("Are you an employee? (True/False): "))

print("Name: ",name, "Age: ",age, "Salary: ",salary, "Employee: ",is_employee,)
